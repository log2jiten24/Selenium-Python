mylang = "Python"
#to get the first character inside the string we can use the index value-space is also character
print(mylang[0])
print(mylang[-6])

myName = "My name is Jitendra"
#slicing - wanted only output as Jitendra
print(myName[11:19])

#step count [::3] -then it will try to go to every 3 characters
print(myName[::3])

#reverse the string
print(myName[::-1])